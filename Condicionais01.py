# 1 - LEIAM DOIS NUMEROS E IMPRIMA O MAIOR DELES

numero1 = int(input('informe um numero: '))
numero2 = int(input('informe um numero: '))

if numero1 > numero2:
    print(f'numero1 digitado:{numero1} e numero2 digitado:{numero2} ,numero 1 é maior que o numero 2')
else:
    print(f'numero1 digitado:{numero1} e numero2 digitado:{numero2} ,numero2 é maior que o numero 1')
